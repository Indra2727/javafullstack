package controlflowstatement;

public class DoWhileEx {
    public static void main(String[] args){
        int no =1;
        do{
            System.out.println("hiii");
            no++;
        }while (no<=5);//this condition is true then hiiii print 5 time

        int no1 =15;
        do{
            System.out.println("hlooo");
            no1++;
        }while (no1<5);//this condition is true then hlooo  no print 5 time
    }
}
